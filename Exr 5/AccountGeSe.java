package com.atelier3;

public class AccountGeSe {
    String nom;
    String adresse;
    double solde;
    double interer;

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }


    public String getAdresse() {
        return adresse;
    }

    public void setAdresse(String adresse) {
        this.adresse = adresse;
    }

    public double getSolde() {
        return solde;
    }

    public void setSolde(double solde) {
        this.solde = solde;
    }

    public double getInterer() {
        return interer;
    }

    public void setInterer(double interer) {
        this.interer = interer;
    }
}
